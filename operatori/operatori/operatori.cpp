// operatori.cpp : Se dau 2 numere naturale. Calculați suma, diferenţa, produsul şi câtul lor, în această ordine.
//

#include <iostream>
using namespace std;

int main()
{
	int x = 10;
	int z = 3;

	cout << " suma " << x + z << endl;
	cout << " scadere " << x - z << endl;
	cout << " inmultire " << x * z << endl;
	cout << " impartire " << x / z << endl;


	return 0; 

}

